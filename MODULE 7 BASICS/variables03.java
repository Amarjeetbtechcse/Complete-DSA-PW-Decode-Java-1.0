// This is a simple Java program to demonstrate variable declaration, initialization, and addition
public class variables03 {
    public static void main(String[] args) {
        // Declare and initialize an integer variable x with the value 10
        int x = 10;

        // Declare and initialize an integer variable y with the value 20
        int y = 20;

        // Calculate the sum of x and y, and store it in the variable z
        int z = x + y;

        // Print the value of z to the console
        System.out.println("The value of z is " + z);
    }
}
