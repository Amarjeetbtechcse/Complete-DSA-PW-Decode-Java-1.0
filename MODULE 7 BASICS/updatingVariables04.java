public class updatingVariables04 {
    public static void main(String[] args) {
        int x = 5; // Initialize x with 5
        System.out.println(x); // Print x (5)

        x = 10; // Update x to 10
        System.out.println(x); // Print x (10)

        x = x + 7; // Add 7 to x (now 17)
        System.out.println(x); // Print x (17)

        x = x - 30; // Subtract 30 from x (now -13)
        System.out.println(x); // Print x (-13)
    }
}
